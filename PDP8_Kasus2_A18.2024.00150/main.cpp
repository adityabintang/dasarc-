#include <iostream>

using namespace std;

int main()
{
    // Menampilkan deret pertama dari 0 hingga 9
    cout << "Deret 1: ";
    for (int i = 0; i < 10; i++) {
        cout << i << " ";
    }
    cout << endl;

    // Menampilkan deret kedua dari 1 hingga 10
    cout << "Deret 2: ";
    for (int i = 1; i <= 10; i++) {
        cout << i << " ";
    }
    cout << endl;

    return 0;
}
